import matplotlib.pyplot as plt
import seaborn as sns
from statsmodels.graphics.tsaplots import plot_acf
import matplotlib.collections as collections # Import collections explicitly

def plot_residual_diagnostics(fit, ticker="TICKER", color="green"):
    """
    Residual diagnostic plots for time series models (ARIMA/SARIMA/etc.)

    Parameters:
    -----------
    fit : fitted model object (must have .resid)
    ticker : str
        Stock ticker for plot titles
    color : str
        Plot color
    """
    # =========================
    residuals = fit.resid

    fig, axes = plt.subplots(2, 2, figsize=(16, 10))

    ticker = ticker.upper()

    # 1. Residuals Over Time
    axes[0, 0].plot(residuals, color=color)
    axes[0, 0].axhline(0, linestyle='--', color='black')
    axes[0, 0].set_title(f"{ticker} Residuals Over Time (Zero Mean Check)")
    axes[0, 0].grid(True)

    print(f"{ticker} Residual Mean:", residuals.mean())

    # 2. ACF of Residuals
    plot_acf(residuals.dropna(), lags=40, ax=axes[0, 1])
    # Set color for stems (LineCollection) and confidence interval (PathCollection or PolyCollection)
    for collection_item in axes[0, 1].collections:
        if isinstance(collection_item, collections.LineCollection) or \
           isinstance(collection_item, collections.PathCollection):
            collection_item.set_color(color)

    # Set color for markers/points (Line2D objects often in lines)
    for line_item in axes[0, 1].lines:
        line_item.set_color(color)

    axes[0, 1].set_title(f"{ticker} Residual ACF (Independence Check)")
    axes[0, 1].grid(True)

    # 3. Residual Variance Over Time
    axes[1, 0].plot(residuals, color=color)
    axes[1, 0].set_title(f"{ticker} Residual Variance Over Time (Homoskedasticity Check)")
    axes[1, 0].grid(True)

    # 4. Residual Distribution
    sns.histplot(residuals, kde=True, color=color, ax=axes[1, 1])
    axes[1, 1].set_title(f"{ticker} Residual Distribution (Normality Check)")
    axes[1, 1].grid(True)

    plt.tight_layout()
    plt.show()