import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns
from scipy.stats import norm, kurtosis

def plot_financial_analysis(df, ticker="TICKER", color="green"):
    """
    Full financial visualization suite:
    1. Time series (Close + High-Low)
    2. Distribution analysis (returns + normal comparisons)

    Parameters:
    -----------
    df : pandas.DataFrame
    ticker : str
        Stock ticker name used in plot titles
    color : str
        Primary plot color
    """

    ticker = ticker.upper()

    # FIGURE 1: TIME SERIES
    fig1, axes = plt.subplots(2, 1, figsize=(16, 10))

    # Closing Price
    sns.lineplot(x=df.index,
                 y=df['Close'],
                 color=color,
                 ax=axes[0])

    axes[0].set_title(f"{ticker} Closing Price Over Time")
    axes[0].set_xlabel("Date")
    axes[0].set_ylabel("Closing Price")
    axes[0].grid(True)

    # High-Low Difference
    sns.lineplot(x=df.index,
                 y=df['High_Low'],
                 color=color,
                 ax=axes[1])

    axes[1].set_title(f"{ticker} High-Low Difference Over Time")
    axes[1].set_xlabel("Date")
    axes[1].set_ylabel("High-Low Difference")
    axes[1].grid(True)

    plt.tight_layout()
    plt.show()

    # FIGURE 2: DISTRIBUTIONS
    fig2, axes = plt.subplots(2, 2, figsize=(16, 12))

    # 1. Returns Distribution
    sns.histplot(df['Return'].dropna(),
                 bins=50,
                 kde=True,
                 color=color,
                 label="Returns",
                 ax=axes[0, 0])

    axes[0, 0].set_title(f"{ticker} Weekly Returns Distribution")
    axes[0, 0].set_xlabel("Return (%)")
    axes[0, 0].set_ylabel("Frequency")
    axes[0, 0].legend()
    axes[0, 0].grid(True)

    # 2. Open-Close Diff
    sns.histplot(df['Open_Close_Diff'].dropna(),
                 bins=50,
                 kde=True,
                 color=color,
                 label="Open - Close Diff",
                 ax=axes[0, 1])

    axes[0, 1].set_title(f"{ticker} Open-Close Difference Distribution")
    axes[0, 1].set_xlabel("Open - Close Difference")
    axes[0, 1].set_ylabel("Frequency")
    axes[0, 1].legend()
    axes[0, 1].grid(True)

    # 3. Log Returns vs Normal
    df['Log_Return'] = np.log(df['Close'] / df['Close'].shift(1))
    data = df['Log_Return'].dropna()

    mu, std = data.mean(), data.std(ddof=0)

    sns.histplot(data,
                 bins=50,
                 stat="density",
                 kde=True,
                 color=color,
                 label="Log Returns",
                 ax=axes[1, 0])

    x = np.linspace(data.min(), data.max(), 500)
    axes[1, 0].plot(x, norm.pdf(x, mu, std),
                    color="red",
                    linewidth=2,
                    label="Normal Distribution")

    axes[1, 0].set_title(f"{ticker} Log Returns vs Normal Distribution")
    axes[1, 0].set_xlabel("Log Return")
    axes[1, 0].set_ylabel("Density")
    axes[1, 0].legend()
    axes[1, 0].grid(True)

    print(f"{ticker} Log Returns Excess Kurtosis:", kurtosis(data, fisher=True))

    # 4. Open-Close vs Normal
    data = df['Open_Close_Diff'].dropna()

    mu, std = data.mean(), data.std(ddof=0)

    sns.histplot(data,
                 bins=50,
                 stat="density",
                 kde=True,
                 color=color,
                 label="Open - Close Diff",
                 ax=axes[1, 1])

    x = np.linspace(data.min(), data.max(), 500)
    axes[1, 1].plot(x, norm.pdf(x, mu, std),
                    color="red",
                    linewidth=2,
                    label="Normal Distribution")

    axes[1, 1].set_title(f"{ticker} Open-Close Diff vs Normal Distribution")
    axes[1, 1].set_xlabel("Open - Close Difference")
    axes[1, 1].set_ylabel("Density")
    axes[1, 1].legend()
    axes[1, 1].grid(True)

    print(f"{ticker} Open-Close Diff Excess Kurtosis:", kurtosis(data, fisher=True))

    plt.tight_layout()
    plt.show()

def plot_forecast(
    df,
    train,
    common_test_actual,
    forecasts: dict,
    ticker=None,
    template="plotly_dark",
    test_color="blue",
    actual_color="gray"
):
    """
    Robust multi-model forecast comparison using Plotly.
    """

    import plotly.graph_objects as go
    import pandas as pd

    fig = go.Figure()

    # --- Step 1: Normalize forecasts ---
    processed = {}

    for name, value in forecasts.items():

        if isinstance(value, tuple):
            series, color = value
        else:
            series, color = value, None

        if not hasattr(series, "index"):
            series = pd.Series(
                series,
                index=common_test_actual.index[-len(series):]
            )

        processed[name] = (series, color)

    # --- Step 2: Find overlapping index ---
    common_index = common_test_actual.index
    for series, _ in processed.values():
        common_index = common_index.intersection(series.index)

    # --- Step 3: Full actual data ---
    fig.add_trace(go.Scatter(
        x=df.index,
        y=df['Close'],
        mode='lines',
        name='Full Actual Data',
        line=dict(color=actual_color)
    ))

    # --- Step 4: Test actual ---
    test_actual = df['Close'].loc[common_index]

    fig.add_trace(go.Scatter(
        x=test_actual.index,
        y=test_actual,
        mode='lines',
        name='Test Actual',
        line=dict(color=test_color)
    ))

    # --- Step 5: Forecasts ---
    for name, (series, color) in processed.items():

        aligned = series.loc[common_index]

        line_style = dict(dash='dash')
        if color:
            line_style["color"] = color

        fig.add_trace(go.Scatter(
            x=aligned.index,
            y=aligned,
            mode='lines',
            name=name,
            line=line_style
        ))

    # --- Title ---
    title = (
        f"All Models Forecast vs. Actual ({ticker} Close Price)"
        if ticker else
        "All Models Forecast vs. Actual"
    )

    fig.update_layout(
        title=title,
        xaxis_title="Date",
        yaxis_title="Close Price",
        hovermode="x unified",
        template=template,
        height=600
    )

    return fig